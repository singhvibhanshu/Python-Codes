# Iterating through a list using a for loop in Python

# Defining a list of fruits
fruits = ["apples", "banana", "mango", "pears", "strawberry", "blueberry"]
# A list is a collection of items in a specific order, enclosed in square brackets.

# Using a for loop to iterate over the list
for x in fruits:  
    # The loop iterates through each item in the list, assigning it to the variable 'x' one at a time.
    print(x)  
    # Prints each fruit in the list.

# Output:
# apples
# banana
# mango
# pears
# strawberry
# blueberry
