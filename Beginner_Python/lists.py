# Demonstrating how to work with lists in Python
# Lists are dynamic data structures that can be modified by adding, removing, or updating items.

# Initializing a list with two items
fruits = ["apples", "banana"]  
# A list of fruits containing "apples" and "banana".

# Printing the initial list
print(fruits)  
# Output: ['apples', 'banana']

# Adding items to the list using the append() method
fruits.append("mango")  # Adds "mango" to the end of the list.
fruits.append("pears")  # Adds "pears" to the end of the list.

# Printing the updated list
print(fruits)  
# Output: ['apples', 'banana', 'mango', 'pears']
