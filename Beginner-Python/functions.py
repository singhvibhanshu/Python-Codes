# Function in Python
# A function is a reusable block of code designed to perform a specific task.

# Defining a function named addTwo
def addTwo(x):  
    # 'x' is the parameter of the function. It acts as a placeholder for the value passed to the function.
    return x + 2  # The function returns the value of x plus 2.

# Calling the function and printing the result
print(addTwo(7))  
# '7' is the argument passed to the function. The function replaces 'x' with '7' and returns 7 + 2.
# Output: 9
