# Data Types in Python
# Python provides various data types to define the nature of a variable.

# Variable declarations with different data types
a = 0          # Integer: Whole number (positive or negative) without a decimal point
b = 1          # Integer
c = -1         # Integer
d = "Vibhanshu"  # String: A sequence of characters enclosed in double quotes
e = 'Y'        # String: A single character can also be represented as a string (enclosed in single or double quotes)
f = 0.32       # Float: A number with a decimal point
g = True       # Boolean: Represents one of two values: True or False

# Checking and printing the data types of the variables using the type() function
print(type(a))  # Output: <class 'int'> (Integer)
print(type(b))  # Output: <class 'int'> (Integer)
print(type(c))  # Output: <class 'int'> (Integer)
print(type(d))  # Output: <class 'str'> (String)
print(type(e))  # Output: <class 'str'> (String)
print(type(f))  # Output: <class 'float'> (Floating-point number)
print(type(g))  # Output: <class 'bool'> (Boolean)
