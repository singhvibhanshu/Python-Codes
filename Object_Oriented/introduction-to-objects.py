# Assigning values to variables x and y
x = 5  # x is an integer (instance of the int class) with a value of 5
y = "string"  # y is a string (instance of the str class) with a value of "string"

# In Python, everything is an object, including variables like x and y.
# When we say an object is an instance of a class, we are referring to the fact that the object is created from that class.

# Demonstrating a method call on the string object 'y'
print(y.upper())  # Calling the 'upper' method on the string object 'y', which converts the string to uppercase
# Output: STRING
